	          <h2> A Lei da Busca Imediata</h2>
	          <p>Não é necessário esperar 24 horas para registrar o desaparecimento de uma criança ou adolescente. Procure imediatamente uma Delegacia de Polícia Civil mais próxima a sua residência para fazer o Boletim de Ocorrência no caso do desaparecimento de uma criança ou adolescente.</br></br>
			  A Lei nº 11.259 de 30 de dezembro 2005, que alterou o Estatuto da Criança e do Adolescente no seu artigo 208 determina a investigação policial imediata em casos de desaparecimento de crianças e adolescentes. A Lei é conhecida como “Lei da Busca Imediata”.</br></br>
			  A investigação do desaparecimento de crianças ou adolescentes será realizada imediatamente após notificação aos órgãos competentes, que deverão comunicar o fato aos portos, aeroportos, Polícia Rodoviária e companhias de transporte interestaduais e internacionais, fornecendo-lhes todos os dados necessários à identificação do desaparecido.</br></br>
	          É importante ressaltar que registro do caso de um desaparecimento no Cadastro Nacional de Crianças e Adolescentes Desaparecidos não substitui o Boletim de Ocorrência, pois este é uma ferramenta que contribui na busca e localização do desaparecido, sendo o Boletim de Ocorrência o instrumento que desencadeia oficialmente a investigação de um desaparecimento.</p>
