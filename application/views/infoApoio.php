	          <h2>Semana de Mobilização Nacional para Busca e Defesa da Criança Desaparecida</h2>
	          <p>Com a sanção da Lei nº 12.393 de 04 de março de 2011 foi instituída a Semana de Mobilização Nacional para Busca e Defesa da Criança Desaparecida, que acontece anualmente no Brasil de 25 a 31 de março.</br></br>
				Nesse período a Secretaria de Direitos Humanos da Presidência da República – SDH/PR por meio da Secretaria Nacional de Promoção dos Direitos da Criança e do Adolescente/SNPDCA, juntamente com a Rede Nacional de Identificação e Localização de Crianças e Adolescentes Desaparecidos – ReDESAP, busca desenvolver ações de mobilização nacional pela busca de crianças e adolescentes desaparecidos, bem como a divulgação de ferramentas e legislação que auxiliam a sociedade no enfrentamento do fenômeno do desaparecimento.</br></br>
				Essas ações também ocorrem no dia 25 de maio, Dia Internacional das Crianças Desaparecidas, contando com a parceria do Centro Internacional para Crianças Desaparecidas e Exploradas (ICMEC).</br></br>
				As ações desenvolvidas buscam assegurar as nossas crianças e adolescentes o direito à convivência familiar e comunitária, direito único e irrevogável garantido na Legislação brasileira.
	</p>
