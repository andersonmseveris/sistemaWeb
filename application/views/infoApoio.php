	          <h2>Disque Direitos Humanos - 100</h2>
	          <p>O Disque 100, além de ser um canal de denúncia de violações de direitos humanos constitui-se também em uma ferramenta que auxilia na localização de crianças e adolescentes desaparecidos.</br></br>
				Por meio do Dique 100 é possível informar o desaparecimento de uma criança ou adolescente ou mesmo prestar informações sobre a localização. O Disque 100 encaminha a informação para os órgãos competentes para que as devidas providências sejam tomadas.</br></br>
				A ligação é gratuita e  você não precisa informar seus dados caso não queira. O serviço está disponível 24 horas, 07 dias por semana. </p>
